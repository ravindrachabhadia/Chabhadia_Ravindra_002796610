    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
      int currentRowIndex = tblResources.getSelectedRow();
        
        if(currentRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        VitalSigns currentResource =  (VitalSigns) model.getValueAt(currentRowIndex, 0);
        
        TextVitalSignId.setText(String.valueOf(currentResource.getVitalSignsId()));
        TextDoctorId.setText(String.valueOf(currentResource.getDoctorId()));
        TextHospitalId.setText(String.valueOf(currentResource.getHospitalId()));
        TextPersonId.setText(String.valueOf(currentResource.getPid()));
        TextVitalSignsPulse.setText(String.valueOf(currentResource.getPulse()));
        TextVitalSignsRespirationRate.setText(String.valueOf(currentResource.getRespirationRate()));
        TextVitalSignsBloodPressure.setText(String.valueOf(currentResource.getTemperature()));
        TextVitalSignsTemperature.setText(String.valueOf(currentResource.getTemperature()));
        TextVitalSignsDate.setText(currentResource.getDate());
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        int currentRowIndex = tblResources.getSelectedRow();
        
        if(currentRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        VitalSigns resource =  (VitalSigns) model.getValueAt(currentRowIndex, 0);
        
        resource.setVitalSignsId(Integer.parseInt(TextVitalSignId.getText()));
        resource.setDoctorId(Integer.parseInt(TextDoctorId.getText()));
        resource.setHospitalId(Integer.parseInt(TextHospitalId.getText()));
        resource.setPid(Integer.parseInt(TextPersonId.getText()));       
        resource.setPulse(Integer.parseInt(TextVitalSignsPulse.getText()));
        resource.setRespirationRate(Integer.parseInt(TextVitalSignsRespirationRate.getText()));        
        resource.setBloodPressure(Double.parseDouble(TextVitalSignsBloodPressure.getText()));
        resource.setTemperature(Double.parseDouble(TextVitalSignsTemperature.getText()));   
        resource.setDate(TextVitalSignsDate.getText());
        
        JOptionPane.showMessageDialog(this,"Successfully updated");
        
        TextVitalSignId.setText("");
        TextDoctorId.setText("");
        TextHospitalId.setText("");
        TextPersonId.setText("");
        TextVitalSignsPulse.setText("");
        TextVitalSignsRespirationRate.setText("");
        TextVitalSignsBloodPressure.setText("");
        TextVitalSignsTemperature.setText("");
        TextVitalSignsDate.setText("");
        
        addDataToTableFromStore();
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int currentRowIndex = tblResources.getSelectedRow();
        
        if(currentRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        VitalSigns currentResource =  (VitalSigns) model.getValueAt(currentRowIndex, 0);
        
        vitalSignsStore.deleteResource(currentResource);
        
        JOptionPane.showMessageDialog(this, "Resource successfully deleted");
        
        addDataToTableFromStore();
        
    }//GEN-LAST:event_btnDeleteActionPerformed


