    private void addDataToTableFromStore() {
        
        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        model.setRowCount(0);
        
        String communityIdInSystem = String.valueOf(systemAdmin.getCurrentCommunityId());
        String userInSystem = systemAdmin.getCurrentUser();
        String hospitalIdInSystem = String.valueOf(systemAdmin.getCurrentHospitalId());
        String userIDInSystem = String.valueOf(systemAdmin.getCurrentUserId());
        
        for (VitalSigns resource : vitalSignsStore.getStore()) {
            Object[] row = new Object[11];
            row[0] = resource;
            row[1] = resource.getPid();
            row[2] = resource.getHospitalId();
            row[3] = resource.getDoctorId();
            row[4] = resource.getTemperature();
            row[5] = resource.getBloodPressure();
            row[6] = resource.getPulse();
            row[7] = resource.getRespirationRate();
            row[8] = resource.getDate();
            
            if("Person".matches(userInSystem)) {
                if(userIDInSystem.matches(String.valueOf(resource.getPid())) ) {
                       model.addRow(row);
                }
            } else if("Hospital Admin".matches(userInSystem)) {
                 if(hospitalIdInSystem.matches(String.valueOf(resource.getHospitalId())) ) {
                       model.addRow(row);
                }
            } else if("Community Admin".matches(userInSystem)) {
                model.addRow(row);
            }
            
            if("System Admin".matches(userInSystem)) {
                model.addRow(row);
            }
            
            
        }
    }

