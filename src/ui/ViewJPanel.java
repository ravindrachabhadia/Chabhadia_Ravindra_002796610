    private void populateTable() {
        
     DefaultTableModel model=(DefaultTableModel)tblEmployee.getModel();
     model.setRowCount(0);
     
     for(Manager m:history.getHistory() ) {
         Object[] row= new Object[4];
         row[0]=m;
         row[1]=m.getEmployeeId();
         row[2]=m.getLevel();
         row[3]=m.getNumber();
      
         model.addRow(row); 
         
                 
                    }
     
    
    
   
                 
                 
                 }
