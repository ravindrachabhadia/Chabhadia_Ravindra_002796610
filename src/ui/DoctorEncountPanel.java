    private void addDataToTableFromStore() {
        
        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        model.setRowCount(0);
        
        
        for (VitalSigns resource : vitalSignsStore.getStore()) {
            Object[] row = new Object[11];
            row[0] = resource;
            row[1] = resource.getPid();
            row[2] = resource.getHospitalId();
            row[3] = resource.getDoctorId();
            row[4] = resource.getTemperature();
            row[5] = resource.getBloodPressure();
            row[6] = resource.getPulse();
            row[7] = resource.getRespirationRate();
            row[8] = resource.getDate();
        
            if(String.valueOf(systemAdmin.getCurrentUserId()).matches(String.valueOf(resource.getDoctorId()))) {
                model.addRow(row);
            }
            
            
        }
    }

