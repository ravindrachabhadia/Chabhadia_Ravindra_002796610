    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int currentRowIndex = tblResources.getSelectedRow();

        if(currentRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        VitalSigns currentResource =  (VitalSigns) model.getValueAt(currentRowIndex, 0);

        lblPersonId.setText(String.valueOf(currentResource.getPid()));
        lblTemp.setText(String.valueOf(currentResource.getTemperature()));
        lblBP.setText(String.valueOf(currentResource.getBloodPressure()));
        lblPulse.setText(String.valueOf(currentResource.getPulse()));
        lblRR.setText(String.valueOf(currentResource.getRespirationRate()));
        
        for (Encounter resource : encounterStore.getStore()) {
               if(String.valueOf(resource.getViralSingsId()).matches(String.valueOf(currentResource.getVitalSignsId()))) {
                    TextEncounterDate.setText(resource.getDate());
                    TextEncounterDetails.setText(resource.getDetails());
                    TextEncounterSuggestions.setText(resource.getSuggestions());
                    TextEncounterNext.setText(resource.getNextEnounter());
               }
        }
        
        
        
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        int currentRowIndex = tblResources.getSelectedRow();

        if(currentRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblResources.getModel();
        VitalSigns currentResource =  (VitalSigns) model.getValueAt(currentRowIndex, 0);

        Encounter resource = encounterStore.addNewResource();

        resource.setPid(currentResource.getPid());
        resource.setDoctorId(currentResource.getDoctorId());
        resource.setViralSingsId(currentResource.getVitalSignsId());
        resource.setDate(TextEncounterDate.getText());
        resource.setDetails(TextEncounterDetails.getText());
        resource.setSuggestions(TextEncounterSuggestions.getText());
        resource.setNextEnounter(TextEncounterNext.getText());

        JOptionPane.showMessageDialog(this,"Successfully added");

        TextEncounterDate.setText("");
        TextEncounterDetails.setText("");
        TextEncounterSuggestions.setText("");
        TextEncounterNext.setText("");
    }//GEN-LAST:event_btnSubmitActionPerformed


